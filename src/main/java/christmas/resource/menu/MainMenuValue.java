package christmas.resource.menu;

public enum MainMenuValue implements MenuValue{
    T_BORN_STAKE("티본스테이크", 55000),
    BARBECUE_RIBS("바비큐립", 54000),
    SEAFOOD_PASTA("해산물파스타", 35000);

    private String name;
    private int price;

    MainMenuValue(String name, int price) {
        this.name = name;
        this.price = price;
    }

    @Override
    public String getName() {
        return this.name;
    }

    @Override
    public int getPrice() {
        return this.price;
    }
}
